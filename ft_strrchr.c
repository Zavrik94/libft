char	*ft_strrchr(const char *str, int n)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	while (i >= 0)
	{
		if (str[i] != n)
			i--;
		if (str[i] == n)
			return ((char *)str + i);
	}
	return (0);
}
