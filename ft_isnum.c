#include "libft.h"

int		ft_isnum(char const *s)
{
	int		i;

	if (!s)
		return (0);
	i = 0;
	if (ft_isdigit(s[i]) == 0 || s[i] == '+' || s[i] == '-')
		while (s[++i])
			if (ft_isdigit(s[i]) == 0)
				return (0);
	if (!s[i])
		return (1);
	return (0);
}
