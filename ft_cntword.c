int		ft_cntword(char const *s)
{
	int	i;
	int cword;

	i = -1;
	cword = 0;
	if (!s)
		return (0);
	while (s[++i] != '\0')
		if (s[i] != ' ' && i == 0)
			cword++;
		else if ((s[i] != ' ' && s[i - 1] == ' '))
			cword++;
	return (cword);
}
